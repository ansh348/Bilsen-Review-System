#!/usr/bin/env node
/**
 * Local-dev seed: rewrite every user's password in data/users.json to a single
 * shared known plaintext, so you can sign in as any seeded account for demos
 * and end-to-end testing.
 *
 * Also regenerates scripts/SEED_CREDENTIALS.md with the full email list grouped
 * by role, so there's a quick reference for "who can I log in as?".
 *
 * NOT for production. The shared password is committed into this script.
 *
 * Run with: node scripts/seed-fake-users.mjs
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import bcrypt from "bcryptjs";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT = path.resolve(__dirname, "..");

const USERS_PATH = path.join(ROOT, "data", "users.json");
const CREDS_PATH = path.join(ROOT, "scripts", "SEED_CREDENTIALS.md");

const SHARED_PASSWORD = "password123";
const BCRYPT_COST = 10;

function readJson(p) {
  return JSON.parse(fs.readFileSync(p, "utf8"));
}

function writeJson(p, data) {
  fs.writeFileSync(p, JSON.stringify(data, null, 2) + "\n", "utf8");
}

function rewritePasswords(users, hash) {
  const now = new Date().toISOString();
  return users.map((user) => ({
    ...user,
    password: hash,
    updatedAt: now,
  }));
}

function buildCredentialsDoc(users) {
  const coordinators = users.filter((u) => u.role === "COORDINATOR");
  const members = users.filter((u) => u.role === "MEMBER");

  const lines = [
    "# Seed Credentials",
    "",
    "Local-dev reference. Generated by `scripts/seed-fake-users.mjs`.",
    "Do not deploy or share — these are not real production credentials.",
    "",
    `**Shared password:** \`${SHARED_PASSWORD}\` (same for every account below)`,
    "",
    `Total accounts: **${users.length}** (${coordinators.length} coordinator, ${members.length} member)`,
    "",
    "## Coordinators",
    "",
    "| Name | Email |",
    "| --- | --- |",
    ...coordinators.map((u) => `| ${u.name} | \`${u.email}\` |`),
    "",
    "## Members",
    "",
    "| Name | Email |",
    "| --- | --- |",
    ...members.map((u) => `| ${u.name} | \`${u.email}\` |`),
    "",
  ];

  return lines.join("\n");
}

async function main() {
  const users = readJson(USERS_PATH);
  if (!Array.isArray(users) || users.length === 0) {
    throw new Error(`No users found at ${USERS_PATH}`);
  }

  const hash = await bcrypt.hash(SHARED_PASSWORD, BCRYPT_COST);
  const updated = rewritePasswords(users, hash);
  writeJson(USERS_PATH, updated);

  const doc = buildCredentialsDoc(updated);
  fs.writeFileSync(CREDS_PATH, doc, "utf8");

  console.log(
    `[seed-fake-users] updated ${updated.length} users in ${path.relative(ROOT, USERS_PATH)}`,
  );
  console.log(`  shared password: ${SHARED_PASSWORD}`);
  console.log(`  reference list:  ${path.relative(ROOT, CREDS_PATH)}`);
}

main().catch((err) => {
  console.error("[seed-fake-users] failed:", err);
  process.exit(1);
});
